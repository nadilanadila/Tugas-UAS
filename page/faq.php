      <p>1. Efisiensi</p>
      <p>2. Kemudahan dan Cepat</p>
      <p>3. Pemisahanan Konten Mudah</p>
      <p>4. Penambahan Fungsi</p>
      <p>5. Dapat Menambahkan Banyak User</p>
      <p>6. Memudahkan Saat Proses Maintenance</p>
      <p>7. Ingin mengubah desain? Sangat Simpel!</p>
     </div>
  </li>

  <li>
    <div class="collapsible-header">Bahasa Pemergraman apa saja untuk membuat CMS?</div>
      <div class="collapsible-body">Software untuk membuat website terus berkembang. 
        Kehadiran aplikasi web kategori CMS (Content Management System) telah merubah bagaimana website 
        dapat dibuat dengan lebih mudah. Anda dapat menggunakan CMS untuk membuat website dinamis tanpa 
        harus menguasai semua komponen perangkat lunak di atas. Untuk membuat website dengan CMS, 
        Anda juga tidak perlu menulis kode-code yang rumit. CMS mudah digunakan layaknya menggunakan 
        aplikasi desktop seperti Microsoft Office.
        <p>CMS adalah aplikasi yang menyediakan kemampuan atau fitur kepada setiap pengguna 
          (website administrator) dengan level perizinan berbeda untuk mengelola semua atau sebagian konten, 
          data atau informasi website. Yang dimaksud mengelola di sini meliputi membuat, mengedit, menghapus, 
          mempublikasikan, mengarsipkan konten, data atau informasi. Di semua CMS, Anda melakukan pengelolaan 
          melalui panel admin atau area administrator yang disediakan.
        </p>
        <p>Ada banyak pilihan CMS yang bisa digunakan, baik open source atau komersial. CMS-CMS tersebut juga 
        dibuat dengan bahasa pemrograman berbeda. Ada CMS yang berbasis PHP,PHP native, Java, Framework Boostrap, Phyton, ASP.NET, Perl atau 
        Ruby on Rails. Setiap CMS juga mendukung satu atau lebih varian database. Database yang umum digunakan 
        di CMS adalah MySQL, PostgreSQL, MariaDB, Percona, Microsoft SQL Server, SQLite, Oracle dan MonggoDB.</p>
      </div>
  </li>
