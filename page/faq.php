    <div class="collapsible-header">Apa itu CMS?</div>
      <div class="collapsible-body">CMS (Content Manajemen System) adalah sebuah perangkat lunak 
        atau sistem yang mengatur konten pada situs web, bisa situs web yang berupa company profile,
        e-commerce, blog, forum, dan lainnya.
        <p>Jika ada mie instan pada makanan, CMS menjadi pembuatan situs instan yang disediakan di internet.
           CMS dapat anda gunakan meskipun anda tidak mengetahui mengenai pemrograman sama sekali.
           CMS juga menyediakan template layaknya WordPress.</p>
    </div>
  </li>

  <li>
    <div class="collapsible-header">Apa Manfaat CMS untuk Anda?</div>
      <div class="collapsible-body">CMS memberikan kemudahan untuk membuat halaman website yang menarik 
      tanpa harus mengerti bagaimana membuat website dari awal. Perkembangan CMS tidak hanya untuk blogger 
      saja melainkan sudah merambah ke bisnis, sehingga saat ini sudah banyak CMS yang ditujukan khusus 
      untuk e-commerce.
      <p>Sebelum membahas mengenai apa itu CMS beserta fungsi-fungsinya, kita lihat dahulu bagaimana 
        sebenarnya perkembangan pengguna internet dan pontensi perkembangan industri e-commerce saat ini
        di Indonesia. Semoga melalui artikel ini nanti sudah bisa menjawab pertanyaan Anda mengenai 
        apa itu CMS dan bagaimana CMS bekerja.</p>
      </div>
  </li>

  <li>
    <div class="collapsible-header">Kenapa Menggunakan CMS?</div>
     <div class="collapsible-body">Ada beberapa alasan yang perlu Anda ketahui sebelum memutuskan 
